const mongoose = require("mongoose");

const userSchema = new mongoose.Schema({
  username:{
    type: String,
    required: true,
    min: 3,
    max: 20,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    max: 50,
  },
  password: {
    type: String,
    required: true,    
    mix: 50,
  },
  isAvatarImageSet: { // Para el avatar del usuario
    type: Boolean,
    default: false,
  },
  avatarImage: {
    type: String,
    default: "",
  },
});

module.exports = mongoose.model("Users", userSchema);